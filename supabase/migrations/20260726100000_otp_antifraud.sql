-- ============================================================================
-- Защита OTP от накрутки SMS-бюджета и перебора кода.
--
-- send-otp и verify-otp не имели вообще никакого ограничения: код — 4 цифры
-- (10 000 вариантов) с окном жизни 5 минут, попытки проверки ничем не
-- ограничены — перебор реалистичен. Отправка SMS тоже ничем не ограничена —
-- при всплеске регистраций общий аккаунт SMSC/Infobip может упереться в свой
-- лимит и заблокироваться, уронив вход для всех разом.
--
-- Клиент УЖЕ ждёт 59 секунд перед повторной отправкой на своей стороне —
-- сервер лишь закрывает дыру для прямых вызовов API в обход интерфейса.
-- ============================================================================

ALTER TABLE public.otp_codes ADD COLUMN IF NOT EXISTS attempts int NOT NULL DEFAULT 0;

COMMENT ON COLUMN public.otp_codes.attempts IS
  'Число неудачных попыток ввода кода. При достижении лимита код считается сожжённым.';
