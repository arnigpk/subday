-- ============================================================================
-- Индекс под ленту SubFlow.
--
-- Лента грузится порциями: ORDER BY created_at DESC + курсор (created_at < X),
-- опционально с фильтром по кофейне. Индексов кроме первичного ключа не было —
-- сейчас в таблице 37 строк и это незаметно, но каждый вызов это seq scan +
-- сортировка. При росте ленты страдает самый горячий экран приложения.
-- ============================================================================

-- Основной путь: общая лента с курсором по времени.
CREATE INDEX IF NOT EXISTS idx_subflow_posts_created_at
  ON public.subflow_posts (created_at DESC);

-- Лента, отфильтрованная по кофейне (SubFlow внутри карточки заведения).
CREATE INDEX IF NOT EXISTS idx_subflow_posts_shop_created_at
  ON public.subflow_posts (shop_id, created_at DESC);
