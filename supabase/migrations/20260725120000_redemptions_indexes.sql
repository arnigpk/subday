-- ============================================================================
-- Индексы под самые тяжёлые запросы по списаниям.
--
-- У redemptions не было ни одного индекса кроме первичного ключа, хотя именно
-- эту таблицу перемалывают: атрибуция рекламы, поведенческий таргет,
-- оценка охвата (подзапрос по каждому профилю) и история пользователя.
-- Сейчас в таблице 275 строк и разницы не видно, но растёт она быстрее всех.
-- Делаем сейчас, пока миграция мгновенная.
-- ============================================================================

-- История пользователя, поведенческий таргет («был / давно не был»).
CREATE INDEX IF NOT EXISTS idx_redemptions_user_time
  ON public.redemptions (user_id, redeemed_at DESC);

-- Атрибуция и отчёты по кофейне.
CREATE INDEX IF NOT EXISTS idx_redemptions_shop_time
  ON public.redemptions (shop_id, redeemed_at DESC);

-- Связка «пользователь + кофейня» — ядро атрибуции: был ли человек здесь
-- раньше и когда пришёл после рекламы.
CREATE INDEX IF NOT EXISTS idx_redemptions_user_shop_time
  ON public.redemptions (user_id, shop_id, redeemed_at);
